<template>
	<v-container>
		<v-layout
			row
			wrap
			justify-center>
			<v-flex
				xs12
				md10
				lg8>
				<ApolloQuery
					:query = "require('@/graphql/problem/list.gql')"
					:variables = "{ page , filter }"
					@result = "onResult" >
					<template
						slot-scope = "{ result: { loading , error , data } }">
						<Searchbar
							:callback = "updateFilter"
							class = "mb-4 fluid"
							label = "" />
						<ErrorSpinner v-if = "error" />
						<div v-else-if = "data">
							<v-hover>
								<v-card
									slot-scope = "{ hover }"
									:class = "`elevation-${hover ? 4 : 1}`"
								>
									<ProblemList
										:problem-item = "data.problemList.problemList"
										:is-loading = "loading"
									/>
								</v-card>
							</v-hover>
							<div class = "text-xs-center mt-3">
								<v-pagination
									ref = "pagination"
									v-model = "page"
									:length = "maxpage"
								/>
							</div>
						</div>
					</template>
				</ApolloQuery>
			</v-flex>
		</v-layout>
	</v-container>
</template>

<script>
import ProblemList from '@/components/problem/list/list';
import Searchbar from '@/components/basic/searchbar';

export default {
	name: 'Problem',
	metaInfo() { return { title: 'Problem' }; },
	components: {
		ProblemList,
		Searchbar,
	},

	data() {
		return {
			page: 1,
			maxpage: 0,
			filter: '',
		};
	},

	activated() {
		if (this.$refs.pagination) { this.$refs.pagination.init(); }
	},

	methods: {
		onResult(result) {
			this.maxpage = result.data.problemList.maxpage;
		},
		updateFilter(result) {
			this.filter = result;
		},
	},
};
</script>
